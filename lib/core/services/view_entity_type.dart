/// Shared entity types used by preference services and UI components.
///
/// This enum defines all entity types that support view preferences,
/// filter preferences, and sort preferences across the application.
enum ViewEntityType {
  goal,
  milestone,
  task,
  habit,
  itinerary,
  trip,
}

/// Entity types that support filter preferences.
enum FilterEntityType {
  goal,
  milestone,
  task,
  habit,
  itinerary,
  trip,
}

/// Entity types that support sort preferences.
enum SortEntityType {
  goal,
  milestone,
  task,
  habit,
}

